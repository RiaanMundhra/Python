# Operations in List
lst = ['mango','grapes','pineapple','apple','banana']
# Length of List
print('length of list:',len(lst))
# First element of List

print(lst[0])

# Append element to List

lst.append('mango')
print(lst)


# Remove element from List

lst.remove('banana')

# Sort List

lst.sort()
print (lst)

# pop element from List

lst.pop()
print(lst)

# reverse List

lst.reverse()
print(lst)


# Slice List

print(lst[1:3])

# Clear List

lst.clear()
print(lst)


