# Data Type = int, float, str, bool

name = "Joyce"

print(type(name))

age = 42

print(type(age))

weight = 60.5

print(type(weight))

isMarried = True

print(type(isMarried))

print("***********************************************")

# Type Casting

# str() - converts any data type to string

age = str(age)

print(type(age))

print(age)

# int() - converts any data type into integer type

weight = int(weight)

print(type(weight))

print(weight)

# float() - converts any data type into float type

age = float(age)

print(type(age))

print(age)